// Supabase Storage base URL for destination images
const SUPABASE_URL = process.env.NEXT_PUBLIC_SUPABASE_URL || 'https://nvhnrtssvdjiefsgilxg.supabase.co';
const BUCKET = 'destination-images';

/**
 * Generate Supabase image URLs for a destination
 * Each destination has ~60 images named {destination}-001.jpg through {destination}-060.jpg
 */
function generateDestinationImages(destinationId: string, count: number = 10): DestinationImage[] {
  return Array.from({ length: count }, (_, i) => ({
    url: `${SUPABASE_URL}/storage/v1/object/public/${BUCKET}/${destinationId}/${destinationId}-${String(i + 1).padStart(3, '0')}.jpg`,
    caption: undefined,
  }));
}

