def main():
    print(f"Processing {len(DESTS)} new destinations...")

    # 1. Read and extend destinations.json
    dest_path = os.path.join(BASE, "data", "destinations.json")
    with open(dest_path, "r", encoding="utf-8") as f:
        existing = json.load(f)
    existing_ids = {d["id"] for d in existing}
    print(f"Existing destinations: {len(existing)}")

    new_dests = []
    for i, d in enumerate(DESTS):
        if d[0] in existing_ids:
            print(f"  SKIP (already exists): {d[0]}")
            continue
        new_dests.append(make_destination_json(d, i))

    existing.extend(new_dests)
    with open(dest_path, "w", encoding="utf-8") as f:
        json.dump(existing, f, indent=2, ensure_ascii=False)
    print(f"Updated destinations.json: {len(existing)} total ({len(new_dests)} new)")

    # 2. Create destination-facts files
    facts_dir = os.path.join(BASE, "data", "destination-facts")
    os.makedirs(facts_dir, exist_ok=True)
    facts_created = 0
    for d in DESTS:
        if d[0] in existing_ids:
            continue
        facts_path = os.path.join(facts_dir, f"{d[0]}.json")
        with open(facts_path, "w", encoding="utf-8") as f:
            json.dump(make_facts_json(d), f, indent=2, ensure_ascii=False)
        facts_created += 1
    print(f"Created {facts_created} destination-facts files")

    # 3. Generate cityPitches entries
    pitches_lines = []
    for d in DESTS:
        if d[0] in existing_ids:
            continue
        pitch = d[17].replace("'", "\\'")
        pitches_lines.append(f"  '{d[0]}':\n    '{pitch}',")
    pitches_output = os.path.join(BASE, "scripts", "new-pitches.txt")
    with open(pitches_output, "w", encoding="utf-8") as f:
        f.write("\n".join(pitches_lines))
    print(f"Wrote {len(pitches_lines)} pitch entries to {pitches_output}")

    # 4. Generate destinationImages entries
    images_lines = []
    for d in DESTS:
        if d[0] in existing_ids:
            continue
        images_lines.append(f"  '{d[0]}': generateDestinationImages('{d[0]}'),")
    images_output = os.path.join(BASE, "scripts", "new-images.txt")
    with open(images_output, "w", encoding="utf-8") as f:
        f.write("\n".join(images_lines))
    print(f"Wrote {len(images_lines)} image entries to {images_output}")

    print("\nDone! Now manually insert:")
    print(f"  1. Contents of {pitches_output} into lib/flash/cityPitches.ts (before closing }};)")
    print(f"  2. Contents of {images_output} into lib/flash/destinationImages.ts (before closing }};)")

if __name__ == "__main__":
    main()
